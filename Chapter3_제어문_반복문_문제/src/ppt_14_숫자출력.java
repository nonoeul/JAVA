
public class ppt_14_숫자출력 {
	public static void main(String[] args) {
		for (int i = 1; i < 6; i++) {
			System.out.println("");
			for (int j = 0; j < 5; j++) {
				System.out.print(i);
			}
		}
		
		System.out.println("");
		System.out.println("======다음======");
		
		for (int i = 1; i < 6; i++) {
			System.out.println();
			for (int j = 1; j < 6; j++) {
				System.out.print(j);
			}
		}
		
		
	}
}
