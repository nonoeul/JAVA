
public class 문제a12 {
	public static void main(String[] args) {
		
		for (int i = 2; i < 30; i+=2) {
			System.out.print(i+" ");
			i+=2;
			System.out.print(i+" ");
			i+=2;
			System.out.printf("%2d\t",i);
			//2d는 모든 문자를 두자리로 출력해라
			//t는 줄 맟춤하는 것이다.
			System.out.println();
			
		}

	
	
	
	}	
}
